import math


def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def absolute(a):
    return abs(a)


def divide(a, b):
    return a / b


def multiply(a, b):
    return a * b


def factorial(a):
    return math.factorial(a)


def power(a, b):
    return math.pow(a, b)


def square(a, b):
    return math.pow(a, 1. / b)


def sqrt(a):
    return math.sqrt(a)

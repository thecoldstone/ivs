import math

class Math(object):
    def Add(a, b):
        return a + b

    def Sub(a, b):
        return a - b

    def Abs(a):
        return math.fabs(a)

    def Divison(a, b):
        return a / b

    def Multiply(a, b):
        return a * b

    def Factorial(a):
        return math.factorial(a)

    def Power(a, b):
        return math.pow(a, b)

    def Square(a, b):
        return math.pow(a,1./b)

